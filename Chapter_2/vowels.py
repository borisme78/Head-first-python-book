# === Типи даних ===
# list (список) - впорядкована колекція голосних літер
# str (рядок) - слово для пошуку голосних

# === Оператори ===
# in - перевірка наявності елемента в колекції

vowels = ["a", "e", "i", "o", "u"]

word = "Milliways"

# Цикл for - ітерація по кожній літері слова
for letter in word:
    # Перевірка: чи літера є голосною
    if letter in vowels:
        # Виводимо голосну літеру
        print(letter)







# === Типи даних ===
# str (рядок) - послідовність символів
# list (список) - впорядкована колекція елементів

# === Функції ===
# list() - перетворює рядок у список символів
# print() - виводить дані на екран

# === Спеціальні символи ===
# \t - табуляція (відступ)

paranoid_android = "Marvin"
# Перетворюємо рядок у список окремих символів
letters = list(paranoid_android)

# Цикл for - ітерація по кожному символу списку
for char in letters:
    # Виводимо кожен символ з табуляцією
    print('\t', char)
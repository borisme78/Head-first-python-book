# === Типи даних ===
# list (список) - впорядкована колекція голосних
# str (рядок) - введене користувачем слово

# === Функції ===
# input() - отримує рядок від користувача

# === Оператори ===
# in - перевірка наявності елемента в колекції
# not in - перевірка відсутності елемента

# === Методи списку ===
# .append(item) - додає елемент в кінець списку

vowels = ["a", "e", "i", "o", "u"]

# Отримуємо слово від користувача
word = input("Provide a word to search for vowels: ")

# Порожній список для зберігання унікальних голосних
found = []

# Зовнішній цикл - перебираємо літери введеного слова
for letter in word:
    # Перевірка: чи літера є голосною
    if letter in vowels:
        # Перевірка: чи голосна ще не додана (уникаємо дублікатів)
        if letter not in found:
            # Додаємо унікальну голосну в список
            found.append(letter)

# Виводимо знайдені унікальні голосні
for vowel in found:
    print(vowel)

        








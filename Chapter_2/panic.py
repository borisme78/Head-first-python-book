# === Типи даних ===
# str (рядок) - послідовність символів
# list (список) - впорядкована колекція елементів

# === Методи списку ===
# .pop() - видаляє та повертає останній елемент (або за індексом)
# .pop(index) - видаляє елемент за вказаним індексом
# .remove(value) - видаляє перший елемент з вказаним значенням
# .extend(iterable) - додає елементи з іншої колекції
# .insert(index, value) - вставляє елемент на вказану позицію

# === Функції ===
# list() - перетворює рядок у список
# ''.join(list) - об'єднує список елементів у рядок
# range(n) - генерує послідовність чисел від 0 до n-1

phrase = "Don't panic!"

# Перетворюємо рядок у список символів
plist = list(phrase)
print(phrase)
print(plist)

# Видаляємо останні 4 символи: "nic!"
for i in range(4):
    plist.pop()

# Видаляємо перший символ: "D"
plist.pop(0)

# Видаляємо символ "'" (апостроф)
plist.remove("'")

# Додаємо два останні символи в кінець: "o", "n"
plist.extend([plist.pop(), plist.pop()])

# Вставляємо елемент на позицію 2, забираючи його з позиції 3
plist.insert(2, plist.pop(3))

# Об'єднуємо список назад у рядок
new_phrase = ''.join(plist)
print(plist)
print(new_phrase)

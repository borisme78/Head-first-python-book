# Цей файл демонструє різницю між незмінними (immutable) та змінними (mutable) типами в Python.
#
# Функція double(arg) працює з незмінними типами (наприклад, числами):
# - Зміни всередині функції не впливають на оригінальний об'єкт зовні.
# - Присвоєння arg = arg * 2 створює новий об'єкт, оригінал залишається незмінним.
#
# Функція change(arg) працює зі змінними типами (наприклад, списками):
# - Зміни всередині функції впливають на оригінальний об'єкт зовні.
# - Метод append() модифікує існуючий об'єкт, зміни зберігаються після функції.
#
# Додаткові різниці:
# - double використовує арифметичну операцію (*), change - метод списку (append).
# - double перепризначає змінну, change змінює об'єкт на місці.

def double(arg):
    # Функція демонструє, що зміни до незмінних типів (як числа) не впливають на оригінал зовні
    print('Before: ', arg)
    arg = arg * 2
    print('After: ', arg)

def change(arg):
    # Функція демонструє, що зміни до змінних типів (як списки) впливають на оригінал зовні
    print('Before: ', arg)
    arg.append('More data')
    print('After: ', arg)
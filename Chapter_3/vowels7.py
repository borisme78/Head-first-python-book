# === Типи даних ===
# Множина (set) - колекція без дублікатів, не зберігає порядок
# Відмінність від списку: set автоматично видаляє повторювані елементи

# === Методи множин ===
# .intersection(set) - повертає спільні елементи двох множин (перетин)
# .union(set) - об'єднує дві множини (всі унікальні елементи)
# .difference(set) - повертає елементи, які є в першій множині, але не в другій

# === Функції ===
# set() - перетворює рядок у множину унікальних символів
# sorted() - сортує елементи множини

# === Переваги множин ===
# Автоматичне видалення дублікатів
# Швидкі операції перетину, об'єднання, різниці

vowels = {"a", "e", "i", "o", "u"}

word = input("Provide a word to search for vowels: ")

# Метод intersection() - знаходить спільні елементи між двома множинами
# set(word) - перетворює введений рядок у множину унікальних літер
found = vowels.intersection(set(word))

# sorted() - сортує знайдені голосні за алфавітом
for vowel in sorted(found):
    print("The vowels found in the word are:", vowel)









# === Типи даних ===
# Словник (dict) - колекція пар ключ-значення, не зберігає порядок
# Список (list) - впорядкована колекція елементів

# === Методи словника ===
# .setdefault(key, default) - встановлює значення за замовчуванням,
#                            якщо ключ ще не існує в словнику
# .items() - повертає представлення (key, value) пар для ітерації

# === Оператори ===
# in - перевірка наявності елемента в колекції

vowels = ["a", "e", "i", "o", "u"]

word = input("Provide a word to search for vowels: ")

found = {}
 
# Цикл for - ітерація по кожній літері слова
for letter in word:
    # Метод setdefault: якщо ключ letter не існує - створює його зі значенням 0
    # Це елегантне рішення проблеми KeyError без ручної ініціалізації
    found.setdefault(letter, 0)
    
    # Перевірка: чи літера є голосною
    if letter in vowels:
        # Якщо голосна - збільшуємо лічильник
        found[letter] += 1

# sorted() - сортує ключі словника за алфавітом
# .items() - повертає пари для розпакування в циклі
for k, v in sorted(found.items()):
    print(k, 'was found', v, 'time(s).')
        









# Тип даних: Список (list) - впорядкована колекція елементів
vowels = ["a", "e", "i", "o", "u"]

# Функція input() - отримання даних від користувача (повертає рядок)
word = input("Provide a word to search for vowels: ")

# Тип даних: Словник (dict) - колекція пар ключ-значення
# Метод: {} - створення порожнього словника
found = {}
 
# Цикл for - ітерація по рядку (рядок - це ітерований об'єкт)
# Метод: in - перевірка наявності елемента в списку
for letter in word:
    # Умовний оператор if - перевірка умови
    if letter in vowels:
        # Операція зі словником: found[letter] - доступ до значення за ключем
        # Оператор += - інкремент (збільшення значення на 1)
        found[letter] += 1

# Метод: sorted() - сортування ітерованого об'єкта
# Метод: .items() - повертає пари (ключ, значення) словника
# Цикл for з розпакуванням кортежу: for k, v in ...
for k, v in sorted(found.items()):
    # Функція print() - виведення даних на екран
    print(k, 'was found', v, 'time(s).')
        







